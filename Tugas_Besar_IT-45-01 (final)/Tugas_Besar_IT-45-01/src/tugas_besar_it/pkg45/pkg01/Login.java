    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        boolean check = false;
        for (int i = 0; i < Users.size(); i++) {
             if(Users.get(i).getEmail().equals(UsernameInput.getText()) && Users.get(i).getPassword().equals(jPasswordField1.getText())){
                 if(Users.get(i).getRole().equals("Mahasiswa")){
                     check = true;
                     MahasiswaView viewmhs = new MahasiswaView();
                     dispose();
                     viewmhs.setVisible(true);
                 } else if (Users.get(i).getRole().equals("Dosen")){
                     check = true;
                     DosenView viewdsn = new DosenView();
                     dispose();
                     viewdsn.setVisible(true);
                 } else if (Users.get(i).getRole().equals("Admin")){
                     check = true;
                     AdminView viewadm = new AdminView();
                     dispose();
                     viewadm.setVisible(true);
                 } 
             }
        }
        if(!check){
             JOptionPane.showMessageDialog(this, "Password atau Username Salah!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

